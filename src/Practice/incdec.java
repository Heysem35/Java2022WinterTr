package Practice;

public class incdec {
    public static void main(String[] args) {


        // increment samples
        int num1 = 10;
        num1++;
        System.out.println(num1);
        num1+=3;
        System.out.println(num1);

        double num2= 20.5;
        num2*= 2;
        System.out.println(num2);

        //decrement samples

        int num4= 20;

        num4/=2;
        System.out.println(num4);

        //postinc
        int a=15;
        int b=++a;
        System.out.println(b);
}
}
